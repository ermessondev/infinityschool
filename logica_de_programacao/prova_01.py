# Exemplos de tipagens de variáveis em Python
# Tipos básicos
inteiro = 10  # Inteiro
flutuante = 3.14  # Float
texto = "Hello, World!"  # String
booleano = True  # Booleano

# Imprimindo os tipos das variáveis
print(f"O tipo de 'inteiro' é: {type(inteiro)}")
print(f"O tipo de 'flutuante' é: {type(flutuante)}")
print(f"O tipo de 'texto' é: {type(texto)}")
print(f"O tipo de 'booleano' é: {type(booleano)}")

# Solicitação de dados ao usuário
nome = input("Por favor, insira o seu nome: ")

# Imprime a solicitação com uma mensagem personalizada
print(f"Olá, {nome}! Seja bem-vindo ao nosso programa.")
